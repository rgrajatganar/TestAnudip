package com.stringdemo;

import java.time.DayOfWeek;

public class WeekD {
	    public static void main(String[] args) {
	        // Loop through each day of the week
	        for (DayOfWeek day : DayOfWeek.values()) {
	            // Print the name of the day
	            System.out.println(day);
	        }
	    }
	}